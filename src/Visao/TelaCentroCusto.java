    public void preencherTabelaCentroCusto(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código ", "Data", "Status", "Descrição"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataAgenda = conecta.rs.getString("DataCentro");
                String dia = dataAgenda.substring(8, 10);
                String mes = dataAgenda.substring(5, 7);
                String ano = dataAgenda.substring(0, 4);
                dataAgenda = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdCentro"), dataAgenda, conecta.rs.getString("StatusCentro"), conecta.rs.getString("DescricaoCentro")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaCentroCusto.setModel(modelo);
        jTabelaCentroCusto.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaCentroCusto.getColumnModel().getColumn(0).setResizable(false);
        jTabelaCentroCusto.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaCentroCusto.getColumnModel().getColumn(1).setResizable(false);
        jTabelaCentroCusto.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTabelaCentroCusto.getColumnModel().getColumn(2).setResizable(false);
        jTabelaCentroCusto.getColumnModel().getColumn(3).setPreferredWidth(250);
        jTabelaCentroCusto.getColumnModel().getColumn(3).setResizable(false);
        jTabelaCentroCusto.setAutoResizeMode(jTabelaCentroCusto.AUTO_RESIZE_OFF);
        jTabelaCentroCusto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

