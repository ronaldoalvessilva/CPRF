    private void jTabelaTipoContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaTipoContaMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaTipoConta.getValueAt(jTabelaTipoConta.getSelectedRow(), 0);
            jCodigoPesq.setText(IdLanc);
            //
            bloquearBotoes();
            bloquearCampos();
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM TIPO_CONTA "
                        + "WHERE IdConta='" + IdLanc + "'");
                conecta.rs.first();
                jCodigo.setText(String.valueOf(conecta.rs.getInt("IdConta")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusConta"));
                jDataRegistro.setDate(conecta.rs.getDate("DataConta"));
                jComboBoxTipoConta.setSelectedItem(conecta.rs.getString("TipoConta"));
                jDescricaoConta.setText(conecta.rs.getString("DescricaoConta"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaTipoContaMouseClicked

