    public void preencherTabelaTipoConta(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código ", "Data", "Status", "Descrição"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataAgenda = conecta.rs.getString("DataConta");
                String dia = dataAgenda.substring(8, 10);
                String mes = dataAgenda.substring(5, 7);
                String ano = dataAgenda.substring(0, 4);
                dataAgenda = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdConta"), dataAgenda, conecta.rs.getString("StatusConta"), conecta.rs.getString("DescricaoConta")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaTipoConta.setModel(modelo);
        jTabelaTipoConta.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaTipoConta.getColumnModel().getColumn(0).setResizable(false);
        jTabelaTipoConta.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaTipoConta.getColumnModel().getColumn(1).setResizable(false);
        jTabelaTipoConta.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTabelaTipoConta.getColumnModel().getColumn(2).setResizable(false);
        jTabelaTipoConta.getColumnModel().getColumn(3).setPreferredWidth(250);
        jTabelaTipoConta.getColumnModel().getColumn(3).setResizable(false);
        jTabelaTipoConta.setAutoResizeMode(jTabelaTipoConta.AUTO_RESIZE_OFF);
        jTabelaTipoConta.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

