    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaTiposContas);
        if (codigoUser == codUserAcesso && nomeTela.equals(telaTiposContas) && codGravar == 1 || nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupo.equals("ADMINISTRADORES")) {
            if (jComboBoxStatus.getSelectedItem().equals("Selecione...")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o status do registro.");
            } else if (jComboBoxTipoConta.getSelectedItem().equals("Selecione...")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o tipo de Conta Receitas/Despesas.");
            } else if (jDescricaoConta.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe a descrição do tipo de conta.");
            } else {
                objTipoConta.setStatusConta((String) jComboBoxStatus.getSelectedItem());
                objTipoConta.setDataConta(jDataRegistro.getDate());
                objTipoConta.setTipoConta((String) jComboBoxTipoConta.getSelectedItem());
                objTipoConta.setDescricaoConta(jDescricaoConta.getText());
                objTipoConta.setObservacao(jObservacao.getText());
                if (acao == 1) {
                    objTipoConta.setUsuarioInsert(nameUser);
                    objTipoConta.setDataInsert(dataModFinal);
                    objTipoConta.setHorarioInsert(horaMov);
                    //
                    control.incluirTipoConta(objTipoConta);
                    buscarCodigo();
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação  
                    bloquearCampos();
                    bloquearBotoes();
                    Salvar();
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
                if (acao == 2) {
                    objTipoConta.setUsuarioUp(nameUser);
                    objTipoConta.setDataUp(dataModFinal);
                    objTipoConta.setHorarioUp(horaMov);
                    //
                    objTipoConta.setIdConta(Integer.valueOf(jCodigo.getText()));
                    control.alterarTipoConta(objTipoConta);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação    
                    bloquearCampos();
                    bloquearBotoes();
                    Salvar();
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem acesso ao registro.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

